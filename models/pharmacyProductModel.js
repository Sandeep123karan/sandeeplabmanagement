// models/pharmacyProductModel.js

const mongoose = require("mongoose");


// ======================================================
// PHARMACY PRODUCT SCHEMA
// ======================================================

const pharmacyProductSchema =
  new mongoose.Schema(

    {

      // ==================================================
      // PHARMACY OWNER
      // ==================================================

      pharmacy: {

        type:
          mongoose.Schema.Types.ObjectId,

        ref: "PharmacyUser",

        required: true,

      },


      // ==================================================
      // PRODUCT TITLE
      // ==================================================

      productTitle: {

        type: String,

        required: true,

        trim: true,

      },


      // ==================================================
      // BRAND
      // ==================================================

      brand: {

        type: String,

        required: true,

      },


      // ==================================================
      // CATEGORY
      // ==================================================

      category: {

        type: String,

        required: true,

      },


      // ==================================================
      // DESCRIPTION
      // ==================================================

      description: {

        type: String,

        default: "",

      },


      // ==================================================
      // MRP
      // ==================================================

      mrp: {

        type: Number,

        required: true,

      },


      // ==================================================
      // DISCOUNT
      // ==================================================

      discount: {

        type: Number,

        default: 0,

      },


      // ==================================================
      // STOCK
      // ==================================================

      stock: {

        type: Number,

        default: 0,

      },


      // ==================================================
      // GST
      // ==================================================

      gst: {

        type: String,

        default: "",

      },


      // ==================================================
      // EXPIRY DATE
      // ==================================================

      expiryDate: {

        type: String,

        default: "",

      },


      // ==================================================
      // PRESCRIPTION REQUIRED
      // ==================================================

      prescriptionRequired: {

        type: Boolean,

        default: false,

      },


      // ==================================================
      // IMAGE
      // ==================================================

      image: {

        type: String,

        default: "",

      },

    },

    {
      timestamps: true,
    }

  );


// ======================================================
// EXPORT MODEL
// ======================================================

module.exports = mongoose.model(
  "PharmacyProduct",
  pharmacyProductSchema
);